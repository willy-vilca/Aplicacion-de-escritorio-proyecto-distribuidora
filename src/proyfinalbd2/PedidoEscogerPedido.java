    private void jButtonModPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModPedidoActionPerformed
        // TODO add your handling code here:
        Date pFecha = jDateChooser.getDate(); // Obtenemos la fecha
        int fila = jTablePedidos.getSelectedRow(); // Obtenemos la fila seleccionada

        // Validamos que se haya seleccionado un pedido
        if (fila < 0) {
            JOptionPane.showMessageDialog(this,
                "Debe seleccionar un pedido.",
                "Error", JOptionPane.ERROR_MESSAGE);
            return; // detenemos el flujo
        }

        int filaReal = jTablePedidos.convertRowIndexToModel(fila);
        id_cliente = modelo.getValueAt(filaReal, 1).toString(); // ID del cliente
        nombre_cliente = modelo.getValueAt(filaReal, 2).toString(); // Nombre del cliente
        id_pedido =Integer.parseInt(modelo.getValueAt(filaReal, 0).toString()); // id del pedido seleccionado

        // Abrimos la ventana siguiente
        new PedidoMOD(this.tablaPedidosMenuPrincipal,this.SORTER,this.BUSCADOR,this.COMBO,this.CONTADOR).setVisible(true);
        this.dispose();

    }//GEN-LAST:event_jButtonModPedidoActionPerformed

