    private void filtrar(TableRowSorter<DefaultTableModel> sorter) {
        String texto = jTextBuscarCliente.getText();
        int columna = jComboPedidoColumnas.getSelectedIndex();
        
        
        if(jComboPedidoColumnas.getSelectedIndex()==3){
        Date pFecha = jDateChooser.getDate();
        long fecha = pFecha.getTime();
        java.sql.Date fecha_sql = new java.sql.Date(fecha);
            texto = String.valueOf(fecha_sql);
            System.out.println(texto);
        }
        if (texto.trim().length() == 0) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + texto, columna));
        }
        
    }    
    
    

    
