    private void jButtonQuitarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonQuitarPedidoActionPerformed
        // TODO add your handling code here:
            int fila = jTableRuta.getSelectedRow();

        if (fila >= 0) {
            int confirmacion = JOptionPane.showConfirmDialog(this,
                "¿Está seguro que desea eliminar este pedido de la ruta?\nEsta acción no se puede deshacer.",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);

            if (confirmacion == JOptionPane.YES_OPTION) {
                DefaultTableModel modeloPedido = (DefaultTableModel) jTableRuta.getModel();
                int filaReal = jTableRuta.convertRowIndexToModel(fila);
                modeloPedido.removeRow(filaReal);

                JOptionPane.showMessageDialog(this,
                    "Pedido eliminado correctamente.",
                    "Éxito", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this,
                "Debe seleccionar un pedido del pedido para eliminar.",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonQuitarPedidoActionPerformed

