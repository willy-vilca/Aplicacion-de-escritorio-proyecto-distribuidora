    private void jButtonRegistrarRutaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarRutaActionPerformed
    DefaultTableModel modeloRuta = (DefaultTableModel) jTableRuta.getModel();
    
    long fecha = rFecha.getTime();
    java.sql.Date fecha_sql = new java.sql.Date(fecha);
    
    
    int idruta = ruta.obtenerUltimoIdRutaRegistrado();
    String nombreRuta = RutasCREAR.nombre;
    java.sql.Date fechaRuta = fecha_sql;          


JOptionPane.showMessageDialog(this,
    "Ruta creada exitosamente.\n\n" +
    "ID Ruta: " + idruta + "\n" +
    "Nombre: " + nombreRuta + "\n" +
    "Fecha de la Ruta: " + fechaRuta,
    "Éxito", JOptionPane.INFORMATION_MESSAGE);


        if (modeloRuta.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this,
                "No hay pedidos asignados en la ruta. Agregue al menos uno.",
                "Pedido vacío", JOptionPane.WARNING_MESSAGE);
            return;
        }

        
        //obtenemos los id de los pedidos asignados en la ruta y los guardamos en la DB en su fila respectiva del pedido
        for (int i = 0; i < modeloRuta.getRowCount(); i++) {
            int idpedido = Integer.parseInt(modeloRuta.getValueAt(i, 0).toString());

            
            //asignamos los pedidos a la ruta en la BD
            ruta.AsignarPedidosARutas(idpedido,idruta);
        }
        

        JOptionPane.showMessageDialog(this,
            "Ruta registrado exitosamente.",
            "Éxito", JOptionPane.INFORMATION_MESSAGE);

        // (Opcional) Limpieza de tabla después del registro
        ruta.mostrarRutasEnTabla(tablaRutas);
        modeloRuta.setRowCount(0);
        // Cerrar solo esta ventana
        dispose();

        
    }//GEN-LAST:event_jButtonRegistrarRutaActionPerformed

