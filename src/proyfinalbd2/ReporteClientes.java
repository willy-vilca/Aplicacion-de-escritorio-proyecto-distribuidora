    public void mostrarClientesEnTabla() {
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ID");
    modelo.addColumn("Nombre");
    modelo.addColumn("Email");
    modelo.addColumn("Tel√©fono");
    modelo.addColumn("Fecha Registro");
    conexion c = new conexion();
    try {
        Connection cn = c.establecerConexion();
            Statement st = cn.createStatement();
        String sql =
                "SELECT \n" +
                "    c.id_cliente AS id,\n" +
                "    'APP - ' || c.nombre AS nombre,\n" +
                "    c.email AS email,\n" +
                "    c.telefono AS telefono,\n" +
                "    c.fecha_registro AS fecha_registro\n" +
                "FROM clientes c\n" +
                "\n" +
                "UNION ALL\n" +
                "\n" +
                "SELECT \n" +
                "    u.id_usuario AS id,\n" +
                "    'WEB - ' || u.nombre AS nombre,\n" +
                "    u.correo AS email,\n" +
                "    u.telefono AS telefono,\n" +
                "    p.fecha_pedido AS fecha_registro\n" +
                "FROM usuarios u\n" +
                "JOIN LATERAL (\n" +
                "    SELECT p2.fecha_pedido\n" +
                "    FROM pedidos p2\n" +
                "    WHERE p2.id_usuario = u.id_usuario\n" +
                "    ORDER BY p2.fecha_pedido DESC\n" +
                "    LIMIT 1\n" +
                ") p ON TRUE\n" +
                "WHERE EXISTS (\n" +
                "    SELECT 1 FROM pedidos px WHERE px.id_usuario = u.id_usuario\n" +
                ")\n" +
                "ORDER BY id ASC;";

        
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            modelo.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("nombre"),
                rs.getString("email"),
                rs.getString("telefono"),
                rs.getDate("fecha_registro")
            });
        }

        tablaClientesReporte.setModel(modelo); // nombre del JTable
    } catch (Exception e) {
        System.out.println("Error al mostrar clientes: " + e.getMessage());
    }
}
    


